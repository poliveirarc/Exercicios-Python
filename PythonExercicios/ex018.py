import math
ang = math.radians(float(input('Digite o ângulo que você deseja: ')))
print('O ângulo {} tem SENO igual a {:.2f}.'.format(ang, math.sin(ang)))
print('O ângulo {} tem COSSENO igual a {:.2f}.'.format(ang, math.cos(ang)))
print('O ângulo {} tem TANGENTE igual a {:.2f}.'.format(ang, math.tan(ang)))